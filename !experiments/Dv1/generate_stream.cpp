
// ============================================================
// FILE: generate_stream.cpp
// ROW INDEXED STREAM GENERATION
// ============================================================

#include <iostream>
#include <vector>
#include <fstream>
#include <filesystem>
#include <opencv2/opencv.hpp>

#include "logger.h"
#include "config.h"

namespace fs = std::filesystem;

// ============================================================
// STRUCTS
// ============================================================

struct RowIndex {

    uint64_t stream_offset;

    uint32_t image_id;

    uint32_t row;

    uint32_t width;
};

// ============================================================
// GLOBALS
// ============================================================

std::vector<uint8_t> universal_stream;

std::vector<RowIndex> row_index;

// ============================================================
// PROCESS IMAGE
// ============================================================

void process_image(
    const std::string& path,
    uint32_t image_id
) {

    cv::Mat img =
        cv::imread(path);

    if (img.empty()) {

        std::cout
            << "Failed: "
            << path
            << "\n";

        return;
    }

    for (int y = 0; y < img.rows; y++) {

        RowIndex idx;

        idx.stream_offset =
            universal_stream.size();

        idx.image_id =
            image_id;

        idx.row =
            y;

        idx.width =
            img.cols;

        row_index.push_back(idx);

        for (int x = 0; x < img.cols; x++) {

            cv::Vec3b p =
                img.at<cv::Vec3b>(y, x);

            universal_stream.push_back(p[0]);
            universal_stream.push_back(p[1]);
            universal_stream.push_back(p[2]);
        }
    }

    std::cout
        << "Processed: "
        << path
        << "\n";
}

// ============================================================
// SAVE STREAM
// ============================================================

void save_stream() {

    std::ofstream out(
        "STREAM/stream.bin",
        std::ios::binary
    );

    out.write(
        (char*)universal_stream.data(),
        universal_stream.size()
    );

    out.close();
}

// ============================================================
// SAVE ROW INDEX
// ============================================================

void save_row_index() {

    std::ofstream out(
        "STREAM/row_index.bin",
        std::ios::binary
    );

    uint64_t count =
        row_index.size();

    out.write(
        (char*)&count,
        sizeof(count)
    );

    out.write(
        (char*)row_index.data(),
        count * sizeof(RowIndex)
    );

    out.close();
}

// ============================================================
// MAIN
// ============================================================

int main() {

    fs::create_directory("STREAM");

    BenchmarkTimer timer;

    timer.tic();

    uint32_t image_id = 0;

    for (
        auto& p :
        fs::directory_iterator("IMG")
    ) {

        process_image(
            p.path().string(),
            image_id++
        );
    }

    save_stream();

    save_row_index();

    double elapsed =
        timer.toc();

    uint64_t stream_size =
        file_size_bytes(
            "STREAM/stream.bin"
        );

    std::cout
        << "\n========== STREAM GENERATION ==========\n";

    print_stat(
        "Generated Byte Count",
        human_size(
            universal_stream.size()
        )
    );

    print_stat(
        "Stream Size",
        human_size(stream_size)
    );

    print_stat(
        "Generation Time",
        elapsed,
        "sec"
    );

    return 0;
}

